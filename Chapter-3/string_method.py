# title()
# Converts the first letter of every word to uppercase.

text = "python programming language"
print("Title : ",text.title())

# strip()
# Removes spaces from start and end of a string.

text2 = "    Hello Vivek     "
print(text2)
print("Strip : ",text2.strip())

# split()
# Splits a string into a list.

text3 = "Python Java JavaScript"
print("Split : ",text3.split())

# find()
# Finds the index position of a word.

text4 = "Hello Vivek are you Python Developer"
print("Find Python : ",text4.find("Python"))

# startswith()
# Checks if the string starts with a specific word.

text5 = "Python Programming"
print("Check Starts With : ",text5.startswith("Python"))

# endswith()
# Checks if the string ends with a specific word.

print("Check Ends with : ",text5.endswith("Programming"))

