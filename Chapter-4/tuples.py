# 2. Python Tuple
# A Tuple bhi list jaisa hota hai but immutable hota hai (change nahi kar sakte).
# Tuple parentheses ( ) me likhte hain.

data = (10,20,30,25,85,25,65,35,65,96,85)
names = ("Vivek","Rahul","Aman")

print("Data :",data)
print("Names :",names)

# count()

print("Repeat numbers 25 : ",data.count(25))

# 2. index()
# The index() method returns the position (index) of the first occurrence of a value.

print("Indes of 25 ",data.index(25))

